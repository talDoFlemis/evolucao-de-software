func (s *catState) cat(r io.Reader, bw *bufio.Writer) error {
	// If no formatting options are set, use the fast path (io.Copy)
	if !s.number && !s.showEnds && !s.showNonprinting && !s.showTabs && !s.squeezeBlank {
		_, err := io.Copy(bw, r)
		return err
	}

	br := bufio.NewReader(r)
	for {
		// 1. Process newlines
		for {
			b, err := br.Peek(1)
			if err != nil {
				if err == io.EOF {
					return nil
				}
				return err
			}
			if b[0] != '\n' {
				break
			}
			// Consume the newline
			_, _ = br.ReadByte()
			if err := s.handleNewline(bw); err != nil {
				return err
			}
		}

		// Read the first non-newline character
		ch, err := br.ReadByte()
		if err != nil {
			if err == io.EOF {
				return nil
			}
			return err
		}

		if s.pendingCR {
			if err := bw.WriteByte('\r'); err != nil {
				return err
			}
			s.pendingCR = false
		}

		if s.newlines >= 0 && s.number {
			s.lineNumber++
			if err := writeLineNumber(bw, s.lineNumber); err != nil {
				return err
			}
		}

		// Process characters until a newline
		for {
			if s.showNonprinting {
				if ch >= 32 {
					if ch < 127 {
						if err := bw.WriteByte(ch); err != nil {
							return err
						}
					} else if ch == 127 {
						if _, err := bw.WriteString("^?"); err != nil {
							return err
						}
					} else {
						if _, err := bw.WriteString("M-"); err != nil {
							return err
						}
						if ch >= 128+32 {
							if ch < 128+127 {
								if err := bw.WriteByte(ch - 128); err != nil {
									return err
								}
							} else {
								if _, err := bw.WriteString("^?"); err != nil {
									return err
								}
							}
						} else {
							if _, err := bw.WriteString("^"); err != nil {
								return err
							}
							if err := bw.WriteByte(ch - 128 + 64); err != nil {
								return err
							}
						}
					}
				} else if ch == '\t' && !s.showTabs {
					if err := bw.WriteByte('\t'); err != nil {
						return err
					}
				} else if ch == '\n' {
					s.newlines = -1
					if err := s.handleNewline(bw); err != nil {
						return err
					}
					break
				} else {
					if _, err := bw.WriteString("^"); err != nil {
						return err
					}
					if err := bw.WriteByte(ch + 64); err != nil {
						return err
					}
				}
			} else {
				if ch == '\t' && s.showTabs {
					if _, err := bw.WriteString("^I"); err != nil {
						return err
					}
				} else if ch != '\n' {
					if ch == '\r' && s.showEnds {
						nextByte, peekErr := br.Peek(1)
						if peekErr == nil && nextByte[0] == '\n' {
							if _, err := bw.WriteString("^M"); err != nil {
								return err
							}
						} else if peekErr == io.EOF {
							s.pendingCR = true
						} else {
							if err := bw.WriteByte('\r'); err != nil {
								return err
							}
						}
					} else {
						if err := bw.WriteByte(ch); err != nil {
							return err
						}
					}
				} else {
					s.newlines = -1
					if err := s.handleNewline(bw); err != nil {
						return err
					}
					break
				}
			}

			var readErr error
			ch, readErr = br.ReadByte()
			if readErr != nil {
				if readErr == io.EOF {
					return nil
				}
				return readErr
			}
		}

	}
}

func (s *catState) handleNewline(bw *bufio.Writer) error {
	s.newlines++
	if s.newlines >= 2 {
		s.newlines = 2
		if s.squeezeBlank {
			return nil
		}
	}

	if s.newlines > 0 && s.number && !s.numberNonblank {
		s.lineNumber++
		if err := writeLineNumber(bw, s.lineNumber); err != nil {
			return err
		}
	}

	if s.showEnds {
		if s.pendingCR {
			if _, err := bw.WriteString("^M"); err != nil {
				return err
			}
			s.pendingCR = false
		}
		if err := bw.WriteByte('$'); err != nil {
			return err
		}
	}

	return bw.WriteByte('\n')
}

func writeLineNumber(w io.Writer, num int) error {
	_, err := fmt.Fprintf(w, "%6d\t", num)
	return err
}

func getOSErrorMessage(err error) string {
	if pe, ok := err.(*os.PathError); ok {
		err = pe.Err
	}
	msg := err.Error()
	if len(msg) > 0 {
		runes := []rune(msg)
		if runes[0] >= 'a' && runes[0] <= 'z' {
			runes[0] = runes[0] - 'a' + 'A'
		}
		msg = string(runes)
	}
	return msg
}

